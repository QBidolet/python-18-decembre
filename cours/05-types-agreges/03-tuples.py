mon_tuple = (1, 2, 3)

print(type(mon_tuple))

# Un élément
mon_tuple = ("salut",)
print(type(mon_tuple))

# Alternative
mon_tuple = 1, 2, 3
print(type(mon_tuple))
mon_tuple = 1,
print(type(mon_tuple))

print(mon_tuple)

# Les paramètres sont des tuples
print(1, 2, 3, 4)

# unpacking
a, b = 1, 2
print(a, b)
